#!/usr/bin/env python3

xterm_colors = [
	(0x00, 0x00, 0x00),
	(0xa8, 0x00, 0x00),
	(0x00, 0xa8, 0x00),
	(0xa8, 0x54, 0x00),
	(0x00, 0x00, 0xa8),
	(0xa8, 0x00, 0xa8),
	(0x00, 0xa8, 0xa8),
	(0xa8, 0xa8, 0xa8),
	(0x54, 0x54, 0x54),
	(0xfc, 0x54, 0x54),
	(0x54, 0xfc, 0x54),
	(0xfc, 0xfc, 0x54),
	(0x54, 0x54, 0xfc),
	(0xfc, 0x54, 0xfc),
	(0x54, 0xfc, 0xfc),
	(0xfc, 0xfc, 0xfc)]

# colors 16..232: the 6x6x6 color cube
_valuerange = (0x00, 0x5f, 0x87, 0xaf, 0xd7, 0xff)
for i in range(217):
	r = _valuerange[(i // 36) % 6]
	g = _valuerange[(i // 6) % 6]
	b = _valuerange[i % 6]
	xterm_colors.append((r, g, b))

# colors 233..253: grayscale
for i in range(1, 24):
	v = 8 + i * 10
	xterm_colors.append((v, v, v))

for r,g,b in xterm_colors:
    print("\t{{{:#04x}, {:#04x}, {:#04x}}},".format(r,g,b))
